package com.st.stmall.pojo;

public class Goods {
	
	private Integer gid;
	private String gname;
	private String gdept;
	private Double gprice;
	private Integer gcount;
	private Integer gsales;
	private Integer gftype;
	private Integer gstype;
	private String gpic;
	private Integer gstatus;
	public Integer getGid() {
		return gid;
	}
	public void setGid(Integer gid) {
		this.gid = gid;
	}
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public String getGdept() {
		return gdept;
	}
	public void setGdept(String gdept) {
		this.gdept = gdept;
	}
	
	
	public Integer getGcount() {
		return gcount;
	}
	public void setGcount(Integer gcount) {
		this.gcount = gcount;
	}
	public Integer getGsales() {
		return gsales;
	}
	public void setGsales(Integer gsales) {
		this.gsales = gsales;
	}
	public Integer getGftype() {
		return gftype;
	}
	public void setGftype(Integer gftype) {
		this.gftype = gftype;
	}
	public Integer getGstype() {
		return gstype;
	}
	public void setGstype(Integer gstype) {
		this.gstype = gstype;
	}
	public String getGpic() {
		return gpic;
	}
	public void setGpic(String gpic) {
		this.gpic = gpic;
	}
	public Integer getGstatus() {
		return gstatus;
	}
	public void setGstatus(Integer gstatus) {
		this.gstatus = gstatus;
	}
	public Double getGprice() {
		return gprice;
	}
	public void setGprice(Double gprice) {
		this.gprice = gprice;
	}
	
}
